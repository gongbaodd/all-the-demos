const yoga = createYoga<Env & ExecutionContext>({
  schema: createSchema({
    typeDefs: /* GraphQL */ `
      type Query {
        hello: String!
      }
    `,
    resolvers: {
      Query: {
        hello: () => "Hello World!",
      },
    },
  }),
  landingPage: false,
  graphqlEndpoint: "/",
});

export default {
  fetch: yoga.fetch,
};
